# --- This function draws a circle
def draw_circle(x, y, radius, stroke_color, fill_color, stroke_width):
    color(stroke_color, fill_color)
    pensize(stroke_width)
    begin_fill()
    penup()
    goto( scale * (x + x0), -1 * scale * (y - y0) - radius)
    pendown()
    circle(radius)
    end_fill()

